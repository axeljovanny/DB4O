    private void btnListar1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnListar1ActionPerformed

        AlumnoControl a = new AlumnoControl();
        try {
            List<Alumno> alumnos = a.verAlumno();

            String[][] grupoArray = new String[alumnos.size()][4];

            if (alumnos.size() > 0) {
                for (int i = 0; i < alumnos.size(); i++) {
                    grupoArray[i][0] = alumnos.get(i).getCveAlu();
                    grupoArray[i][1] = alumnos.get(i).getNomAlu();
                    grupoArray[i][2] = "" + alumnos.get(i).getEdaAlu();
                    grupoArray[i][3] = alumnos.get(i).getCveGru();
                }

                jTable1.setModel(new javax.swing.table.DefaultTableModel(
                    grupoArray,
                    new String[]{"Clave ", "Nombre ", "Edad", "Clave de Grupo"}));
        } else {
            JOptionPane.showMessageDialog(this, "No existe ningún alumno agregado");
        }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Error" + e.getMessage());
        }
        a.cerrarBD();
    }//GEN-LAST:event_btnListar1ActionPerformed

