    private void btnAgregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAgregarActionPerformed
         if (!txt_cveAlu.getText().isEmpty() && !txt_nomAlu.getText().isEmpty()) {
            AlumnoControl g = new AlumnoControl();
            Boolean alumno = g.buscarGrupo(txt_cveAlu.getText());
            System.out.println("resultado de la busqueda de alumno" + alumno);
            if (!alumno == TRUE ) {
        byte edad = Byte.parseByte("" + spnEdad.getValue());
        Alumno a = new Alumno(txt_cveAlu.getText(), txt_nomAlu.getText(), edad, txt_cveGru.getText());
        g.agregarAlumno(a);
        JOptionPane.showMessageDialog(this, "Agregado");
            }else{
              JOptionPane.showMessageDialog(this, "Ya existe un registro con esta clave");      
            }

         }
         if (txt_cveAlu.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Ingrese un clave para el Alumno");
            if (txt_nomAlu.getText().isEmpty()|| txt_cveGru.getText().isEmpty()) {
                JOptionPane.showMessageDialog(this, "Ingrese todos los datos del alumno");
            }
        }
    }//GEN-LAST:event_btnAgregarActionPerformed

